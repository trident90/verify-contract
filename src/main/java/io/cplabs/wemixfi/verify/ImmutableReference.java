package io.cplabs.wemixfi.verify;

public class ImmutableReference {

    private int start;
    private int length;

    // Getters and Setters for each field
    public int getStart() {
        return this.start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getLength() {
        return this.length;
    }

    public void setLength(int length) {
        this.length = length;
    }
}